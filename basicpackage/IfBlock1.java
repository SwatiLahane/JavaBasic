package basicpackage;

public class IfBlock1 {

	public static void main(String[] args)
	{
		int a =10;
	    int b =20;
     
	    //if block will executed all the true conditions in program 
	    //it will check all the conditions n execute only true
     if(a > b) //true 
     {
    	 System.out.println("1");
    	 
     }
     if(a >= b)
     {
    	 System.out.println("2");
    	 
     }
     if(a<b) //true
     {
    	 System.out.println("3");
     }
     if(a!=b)
     {
    	 System.out.println("4");
     }
     
   }
}


