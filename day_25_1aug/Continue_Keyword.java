package day_25_1aug;

public class Continue_Keyword {

	public static void main(String[] args) 
	{
          
		
         for(int i=0; i<=4; i++)
         {
        	 
        	 if(i == 3)   //in output it will skip 3 to skill some itteration we can use continue 
        	 {
        		 continue; 
        	 }
        	 
        	 System.out.println(i);
         }

	}

}
